package xf.ad.model;

public class BasicDataBean {

	private String seatscount;
	private String onlinecount;
	private String terminalcount;

	public String getSeatscount() {
		return seatscount;
	}

	public void setSeatscount(String seatscount) {
		this.seatscount = seatscount;
	}

	public String getOnlinecount() {
		return onlinecount;
	}

	public void setOnlinecount(String onlinecount) {
		this.onlinecount = onlinecount;
	}

	public String getTerminalcount() {
		return terminalcount;
	}

	public void setTerminalcount(String terminalcount) {
		this.terminalcount = terminalcount;
	}
}
